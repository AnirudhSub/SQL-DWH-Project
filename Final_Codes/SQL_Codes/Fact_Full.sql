------------------------------------------------------BRANCH REPORT FILE------------------------------------------------------------------
DROP TABLE IF Exists [DW].[MNTH_SALES_RPT_BRANCH_FILE]
IF OBJECT_ID(N'[DW].[MNTH_SALES_RPT_BRANCH_FILE]' , N'U') IS NULL
	BEGIN
		CREATE TABLE [DW].[MNTH_SALES_RPT_BRANCH_FILE] (
		   BRANCH_CODE NUMERIC(10),
		   BRANCH_NAME VARCHAR(25),
		   TOTAL_REVENUE DECIMAL(12,2),
		   CREATED_DATE DATE
		);
	END	
	MERGE INTO [DW].[MNTH_SALES_RPT_BRANCH_FILE] tgt
	USING (
	   SELECT
		   BRANCH_CODE,
		   BRANCH_NAME,
		   SUM(TOTAL_AMOUNT) AS TOTAL_REVENUE
	   FROM
		   [DW].[CDW_SAPP_F_AGG_DATA]
	   GROUP BY
		   BRANCH_CODE,
		   BRANCH_NAME
	) src ON (tgt.BRANCH_CODE = src.BRANCH_CODE)
	WHEN MATCHED THEN
	   UPDATE SET
		   tgt.TOTAL_REVENUE = src.TOTAL_REVENUE
	WHEN NOT MATCHED THEN
	   INSERT (BRANCH_CODE, BRANCH_NAME, TOTAL_REVENUE, CREATED_DATE)
	   VALUES (src.BRANCH_CODE, src.BRANCH_NAME, src.TOTAL_REVENUE, GETDATE());

