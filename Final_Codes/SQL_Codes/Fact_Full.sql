----------------------------------------------------PRODUCT REPORT FILE----------------------------------------------------------------------
DROP TABLE IF Exists [DW].[MNTH_SALES_RPT_PRODUCT_FILE]
IF OBJECT_ID(N'[DW].[MNTH_SALES_RPT_PRODUCT_FILE]' , N'U') IS NULL
	BEGIN
		CREATE TABLE [DW].[MNTH_SALES_RPT_PRODUCT_FILE] (
		   PRODUCT_CODE NUMERIC(15),
		   PRODUCT_NAME VARCHAR(25),
		   TOTAL_QUANTITY NUMERIC(7),
		   TOTAL_REVENUE DECIMAL(12,2),
		   CREATED_DATE DATE
		);
	END	
		MERGE INTO [DW].[MNTH_SALES_RPT_PRODUCT_FILE] tgt
	USING (
	   SELECT
		   PRODUCT_CODE,
		   PRODUCT_NAME,
		   SUM(TOTAL_QUANTITY) AS TOTAL_QUANTITY,
		   SUM(TOTAL_AMOUNT) AS TOTAL_REVENUE,
		   GETDATE() AS CREATED_DATE 
	   FROM [DW].[CDW_SAPP_F_AGG_DATA]
	   GROUP BY PRODUCT_CODE, PRODUCT_NAME
	) src
	ON (tgt.PRODUCT_CODE = src.PRODUCT_CODE)
	WHEN MATCHED THEN
	   UPDATE SET
		   tgt.TOTAL_QUANTITY = src.TOTAL_QUANTITY,
		   tgt.TOTAL_REVENUE = src.TOTAL_REVENUE,
		   tgt.CREATED_DATE = src.CREATED_DATE
	WHEN NOT MATCHED THEN
	   INSERT (
		   PRODUCT_CODE,
		   PRODUCT_NAME,
		   TOTAL_QUANTITY,
		   TOTAL_REVENUE,
		   CREATED_DATE
	   ) VALUES (
		   src.PRODUCT_CODE,
		   src.PRODUCT_NAME,
		   src.TOTAL_QUANTITY,
		   src.TOTAL_REVENUE,
		   src.CREATED_DATE
	   );
